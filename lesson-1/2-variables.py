# Variables store values and can have several types:
# Integers, floats, strings, booleans, lists, dictionaries, and more.

# First, let's look at strings.
# strings are sequences of characters, enclosed in quotes.

# This is a string. Try changing it's stored value. 
statement_to_print = "Hello, World!" 

print(statement_to_print)

# You can do "operations" on variables.
# For example, you can add two or more variables together.
first_word = "Hello,"
second_word = "World!"
statement_to_print = first_word + " " + second_word
print(statement_to_print)
